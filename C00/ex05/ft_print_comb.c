#include <unistd.h>

void	ft_print_comb(void)
{
	char	x;
	char	y;
	char	z;

	x = '0';
	while (x <= '7')
	{
		y = 1 + x;
		while (y <= '8')
		{
			z = 1 + y;
			while (z <= '9')
			{
				write(1, &x, 1);
				write(1, &y, 1);
				write(1, &z, 1);
				if (!(x == '7' && y == '8' && z == '9'))
					write(1, ", ", 2);
				z++;
			}
			y++;
		}
		x++;
	}
}
