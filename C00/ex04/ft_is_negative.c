#include <unistd.h>

void	ft_is_negative(int n)
{
	char	x;
	char	y;

	x = 78;
	y = 80;
	if (n < 0)
	{
		write(1, &x, 1);
	}
	else
	{
		write(1, &y, 1);
	}
}
